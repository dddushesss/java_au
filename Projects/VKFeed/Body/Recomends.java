package Body;

public class Recomends {
    String PublicAvatarLink;
    String PublicName;
    String PublicLink;
    String PublicDescription;
    String PublicSubscribeLink;

    public Recomends(String publicAvatarLink, String publicName, String publicLink, String publicDescription, String publicSubscribeLink) {
        PublicAvatarLink = publicAvatarLink;
        PublicName = publicName;
        PublicLink = publicLink;
        PublicDescription = publicDescription;
        PublicSubscribeLink = publicSubscribeLink;
    }
}
